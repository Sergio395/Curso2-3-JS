let a = 5;
let b = 7;

console.log(a + b,"adicion");//12
console.log(a -  b,"subtraccion");//-2
console.log(a * b, "multiplicacion");//35
console.log(a / b, "division");//0.7142857142857143
console.log(a % b, "modulo");//5
console.log(a ** b, "potencias");//78125

//incrementar
console.log(++a);//6
console.log(a++);//6 
console.log(a);//7

//decrementar
console.log(--a);//6
console.log(a--);//6 
console.log(a);//5